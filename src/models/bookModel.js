const mongoose = require("mongoose");

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////        BOOK       SCHEMA      /////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

const bookSchema = new mongoose.Schema(
    {
        name: {
            type: String,
            required: true,
            trim: true
        } ,

        author: {
            type: String,
            required: true,
            trim: true
        } ,

        category: {
            type: String,
            required: true,
            trim: true
        }
}, {timestamps:true})

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////       MODULES    EXPORTED     /////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

module.exports = mongoose.model("book", bookSchema)